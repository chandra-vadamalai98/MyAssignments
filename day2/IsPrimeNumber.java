package week1.day2;

public class IsPrimeNumber {

	public static void main(String[] args) {
		int number = 13;
		for(int i = 2; i<=number; i++){
			if(i/number==1) {
				System.out.println("Yes it is prime number"); 
			}
			else System.out.println("Yes it is not prime number");
		
	} 
	}
}

